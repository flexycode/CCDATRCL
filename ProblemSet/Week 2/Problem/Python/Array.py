# Example program that demonstrates the use of an array (using List)
def array_example():
  
    # Declare and initialize an array of integers
    scores = [90, 80, 70, 95, 85]

    # Print the array elements
    print("Array elements:")
    print(scores)

    # Access an array element
    print("Accessing an array element:")
    print(scores[0])

    # Update an array element
    scores[0] = 100
    print("Updated array elements:")
    print(scores)

array_example()
