# Example program that demonstrates the use of a List
def list_example():
    # Create a List of strings
    kpop = ["TripleS", "IU", "Twice", "Exid"]

    # Print the list elements
    print("List elements:")
    print(kpop)

    # Add an element to the list
    kpop.append("BlackPink")
    print("Updated list elements:")
    print(kpop)

    # Remove an element from the list
    kpop.remove("IU")
    print("Updated list elements:")
    print(kpop)

list_example()
